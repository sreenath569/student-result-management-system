    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        String rollno = jTextField1.getText();
        String java = jTextField2.getText();
        String dsa = jTextField3.getText();
        String dbms = jTextField4.getText();
        String web = jTextField5.getText();
        String ai = jTextField6.getText();
        
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/student_result_management_system", "root", "siagb&!@&7127");
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from student where rollno = '"+rollno+"'");
            if(rs.next()){
                st.executeUpdate("insert into result values('"+rollno+"', '"+java+"', '"+dsa+"', '"+dbms+"', '"+web+"', '"+ai+"')");
                JOptionPane.showMessageDialog(null, "Data updated successfully");
                setVisible(false);
                new insertResult().setVisible(true);
            }
            else{
                JOptionPane.showMessageDialog(null, "Student does not exist!");
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_jButton6ActionPerformed

